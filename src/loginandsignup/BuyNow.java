    private void BuyNow()
    {
        String name=txtprocode.getText();
           
           try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        con1=DriverManager.getConnection("jdbc:mysql://localhost/users_databse","root","");
       insert=con1.prepareStatement("Select * from product where barcode=?");
       insert.setString(1, name);
       rs=insert.executeQuery();
       
       while(rs.next()){
           int currentqty ;
           currentqty=rs.getInt("qty");
           
         int price=Integer.parseInt(txtprice.getText());
         int qtynew=Integer.parseInt(txtqty.getText());
         int tot=price*qtynew;
         
         if(qtynew>currentqty ){
             JOptionPane.showMessageDialog(this, "Available Product"+"="+currentqty);
             JOptionPane.showMessageDialog(this, "Product is insufficient");
         }
         else{
             model= (DefaultTableModel)jTable1.getModel();
             model.addRow(new Object[]
             {
                txtprocode.getText(),
               txtproname.getText(),
               txtprice.getText(),
               txtqty.getText(),
               tot,
            
         } );
             int sum=0;
             for(int i=0;i<jTable1.getRowCount();i++){
                 sum=sum+Integer.parseInt(jTable1.getValueAt(i,4).toString());
                 
                 
                
             }
             
              txtsub.setText(Integer.toString(sum));
             
               txtprocode.setText("");
               txtproname.setText("");
               txtprice.setText("");
               txtqty.setText("");  
               txtprocode.requestFocus();
       }
       } 
        }catch(ClassNotFoundException ex){
            Logger.getLogger(Brand.class.getName()).log(Level.SEVERE,null, ex);
            
        } catch (SQLException ex) {
            Logger.getLogger(Brand.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
