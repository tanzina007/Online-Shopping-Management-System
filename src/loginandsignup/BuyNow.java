    private void sales()
    {
    DateTimeFormatter dtf=DateTimeFormatter.ofPattern("yyyy/MM/dd");
    LocalDateTime now=LocalDateTime.now();
    
    String date=dtf.format(now);
    String subtot=txtsub.getText();
    int insertlastid=0;
    
    try{
        Class.forName("com.mysql.cj.jdbc.Driver");
        con1=DriverManager.getConnection("jdbc:mysql://localhost/users_databse","root","");
       insert=con1.prepareStatement("Insert INTO sales(date,subtotal)Values(?,?)");
       insert.setString(1, date);
       insert.setString(2, subtot);
       
       insert.executeUpdate();
       ResultSet generatedKeyResult=insert.getGeneratedKeys();
       
       if(generatedKeyResult.next()){
           insertlastid=generatedKeyResult.getInt(1);
       }
       JOptionPane.showMessageDialog(this, insertlastid);
        }catch(ClassNotFoundException ex){
            Logger.getLogger(Brand.class.getName()).log(Level.SEVERE,null, ex);
            
        } catch (SQLException ex) {
            Logger.getLogger(Brand.class.getName()).log(Level.SEVERE, null, ex);
        }
}
    
    
    
    
    
